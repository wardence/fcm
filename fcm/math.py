"""
A small set of functions for doing math operations.
"""

def add(a, b):
    """
    Function that adds 2 arguements
    """
    return a + b

def mult(a, b):
    """
    Function that multiplies 2 arguements
    """
    return a * b

def div(a, b):
    """
    Function that divides 2 arguements
    """
    a = float(a)
    b = float(b)
    return a / b
